class Animal : 
    def __init__(self,weight,name,age, type):
        self.weight = weight
        self.name = name
        self.age = age
        self.type = type
            
    def getWeight(self) :
        return self.weight

    def getName(self) :
        return self.name
    
    def getAge(self) : 
        return self.age

    def getType(self) :
        return self.type